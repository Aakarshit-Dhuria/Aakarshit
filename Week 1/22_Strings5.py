# String Indexing

s ='python'

print(s[0])
print(s[1])
print(s[2])
print(s[3])
print(s[4])
print(s[5])

# Negative Indexing : To get the reverse of a string
# In this, -1 prints the last letter of the string and so on
print(s[-1])
print(s[-2])
print(s[-3])
print(s[-4])
print(s[-5])
print(s[-6])
