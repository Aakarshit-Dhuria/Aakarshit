s = 'Coffee'
t = 'Bread'

print(s)
print(t)
print(s+t)  # This is called String Concatination

print(s[1])

# String Slicing :
# It will print characters starting from the first number to one less than second number
print(s[1:3])   # In this case, it will print s[1] and s[2]
print(s[1:5])   # In this case, it will print s[1],s[2],s[3],s[4]
